    public ProductReferencesPanel(Collection<ProductReference> prodrefs) {
        try {
            initComponents();

            if (prodrefs==null){
                prodrefs = new ArrayList();
            }

            Vector<String> tableHeaders = GUIUtils.getHeader(jTableProductReferences);
            Vector tableData = new Vector();
            for (ProductReference pr : prodrefs){
                Vector<Object> oneRow = new Vector<Object>();
                oneRow.add(pr.getReferenceType());
                oneRow.add(pr.getValue());
                tableData.add(oneRow);
            }

            List<ProductReferenceType> referenceTypes=(List)DAOCallerAgent.callMethod(ProductReferenceTypeAccessor.class, ProductReferenceTypeAccessor.GET_ALL_PRODUCT_REFERENCE_TYPES);
            for (ProductReferenceType prt : referenceTypes){
                boolean found=false;
                for (ProductReference pr : prodrefs){
                    if (pr.getReferenceType().equals(prt.getReferenceType())) {
                        found =true;
                    }
                }
                if (!found){
                    Vector<Object> oneRow = new Vector<Object>();
                    oneRow.add(prt.getReferenceType());
                    oneRow.add("");
                    tableData.add(oneRow);
                }
            }
            jTableProductReferences.setModel(new MyTableModel(tableData, tableHeaders));
        } catch (Exception ex) {
                logger.error(StringUtils.formatErrorMessage(ex));
        }
    }

    public class MyTableModel extends DefaultTableModel{

        public MyTableModel (Vector tableData,Vector tableHeaders){
            super(tableData, tableHeaders);
        }
        @Override
         public boolean isCellEditable(int rowIndex, int columnIndex){

            /** Or whatever column index you want to be editable */
            return columnIndex == 1;
        }
    }
