package org.gaia.dao.pricing.pricers.isda;

import java.util.Date;

/**
 *
 * @author Benjamin Frerejean
 */
public class TBadDayList {
      /* Bad day list */
   private int     count;                      /* number of items currently in list */
   private int     maxAllowed;                 /* max no of items allowed in arrays */
   private Date[]  badDay;                     /* array of bad business days */
   private Date[]  goodDay;                    /* array of corresponding good ays */

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getMaxAllowed() {
        return maxAllowed;
    }

    public void setMaxAllowed(int maxAllowed) {
        this.maxAllowed = maxAllowed;
    }

    public Date[] getBadDay() {
        return badDay;
    }

    public void setBadDay(Date[] badDay) {
        this.badDay = badDay;
    }

    public Date[] getGoodDay() {
        return goodDay;
    }

    public void setGoodDay(Date[] goodDay) {
        this.goodDay = goodDay;
    }




}
